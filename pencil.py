# Pencil is a writing implement

class Pencil(object):
    def pencil(self):
        return Pencil.pencil

    def writes(self):
        return Pencil.writes


# Paper is the surface the pencil creates text on

class Paper(object):

    def paper(self):
        return Paper.paper

    def text(self):
        return Paper.text
